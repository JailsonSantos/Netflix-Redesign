export const getStaticProps: GetStaticProps = async () => {
  const products = await getProducts(payments, {
    includePrices: true,
    activeOnly: true,
  })
    .then((response) => response)
    .catch((err) => console.log(err.message)
    );

  return {
    props: {
      products,
    }
  }
}